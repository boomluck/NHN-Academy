public class Problem_18 {
    public static void main(String[] args) {
        int[] numbers = { 90, 85, 78, 92 };
        System.out.println("배열 길이: " + numbers.length); // 배열 길이: 4
        System.out.println("첫 번째: " + numbers[0]); // 첫 번째: 90
        System.out.println("마지막: " + numbers[numbers.length - 1]); // 마지막: 92
    }
}
